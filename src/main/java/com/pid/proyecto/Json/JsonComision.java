package com.pid.proyecto.Json;

public class JsonComision {

    private int idResolucion = -1;
    
    private int idPresidente = -1;
    
    private int idSecretario = -1;
    
    private int idRolPresidente = -1;
    
    private int idRolSecretario = -1;


    public JsonComision() {
    }

    public int getIdResolucion() {
        return idResolucion;
    }

    public void setIdResolucion(int idResolucion) {
        this.idResolucion = idResolucion;
    }

    public int getIdPresidente() {
        return idPresidente;
    }

    public void setIdPresidente(int idPresidente) {
        this.idPresidente = idPresidente;
    }

    public int getIdSecretario() {
        return idSecretario;
    }

    public void setIdSecretario(int idSecretario) {
        this.idSecretario = idSecretario;
    }

    public int getIdRolPresidente() {
        return idRolPresidente;
    }

    public void setIdRolPresidente(int idRolPresidente) {
        this.idRolPresidente = idRolPresidente;
    }

    public int getIdRolSecretario() {
        return idRolSecretario;
    }

    public void setIdRolSecretario(int idRolSecretario) {
        this.idRolSecretario = idRolSecretario;
    }

    
}
